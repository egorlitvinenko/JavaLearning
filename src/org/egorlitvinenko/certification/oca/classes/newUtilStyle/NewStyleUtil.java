package org.egorlitvinenko.certification.oca.classes.newUtilStyle;

/**
 * @author Egor Litvinenko
 */
interface NewStyleUtil {

    static int sum(int a, int b) {
        return a + b;
    }

    static int multi(int a, int b) {
        return a * b;
    }

    static int integralDivision(int a, int b) {
        return a / b;
    }

    static int mod(int a, int b) {
        return a % b;
    }

}
